// كل صفحات الأدوار (عدا الحارس) — لخريطة الموقع والروابط الداخلية
const ROLE_PAGES = Object.values(ROLES)
  .filter(r => r.urlSegment)
  .flatMap(r => r.cities.map(slug => ({
    role: r,
    city: CITIES.find(c => c.slug === slug),
    url: `/jobs/${slug}/${r.urlSegment}`,
  })))
  .filter(p => p.city);

// أدوار إضافية متاحة في مدينة معيّنة — تُعرض كروابط داخلية بين الصفحات
function otherRolesIn(citySlug, currentRoleSlug) {
  return Object.values(ROLES)
    .filter(r => r.slug !== currentRoleSlug && r.cities.includes(citySlug))
    .map(r => ({ title: r.title, url: `/jobs/${citySlug}${r.urlSegment ? '/' + r.urlSegment : ''}` }));
}

function renderJobPage(res, city, role) {
  // cache على مستوى المتصفح/الـ CDN — المحتوى ثابت (التواريخ تتجدّد نصف شهرياً)
  res.setHeader('Cache-Control', 'public, max-age=3600');
  res.render('city-jobs', {
    city, role, cities: CITIES,
    otherRoles: otherRolesIn(city.slug, role.slug),
    roleCities: role.cities.map(s => CITIES.find(c => c.slug === s)).filter(Boolean),
  });
}

// GET /jobs/:slug/:role — صفحة مشرف/مدير أمن في مدينة
router.get('/:slug/:role', (req, res, next) => {
  const city = CITIES.find(c => c.slug === req.params.slug);
  const role = Object.values(ROLES).find(r => r.urlSegment === req.params.role);
  // الدور غير معروف، أو غير مفتوح في هذه المدينة → 404 بدل صفحة وهمية
  if (!city || !role || !role.cities.includes(city.slug)) return next();
  renderJobPage(res, city, role);
});

// GET /jobs/:slug — صفحة حارس الأمن (الافتراضي)، وإن لم توجد المدينة يمرّر للـ 404
router.get('/:slug', (req, res, next) => {
  const city = CITIES.find(c => c.slug === req.params.slug);
  if (!city) return next();
  renderJobPage(res, city, ROLES.guard);
});

// تُستخدم في server.js لبناء sitemap.xml ديناميكياً
router.CITIES = CITIES;
router.ROLES = ROLES;
router.ROLE_PAGES = ROLE_PAGES;
module.exports = router;
